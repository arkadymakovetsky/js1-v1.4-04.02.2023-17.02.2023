/*Вывести корректно фразу "В корзине N товаров" для произвольного положительного целого в переменной n 
товар   1,21,31,...121,
товара  2-4, 22-24, 32-34...
товаров 0, 5-20,35-40...
*/
let n = 38;
let w = "товаров";

if (n % 100 < 5 || n % 100 > 20) {
  if (n % 10 == 1) w = "товар";
  if (n % 10 > 1 && n % 10 < 5) w = "товара";
}

console.log(`В корзине ${n} ${w}`);
