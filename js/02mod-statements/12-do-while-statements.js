/*
цикл - инструкция для многократного выполнения блока
do - цикл с постпроверкой условия ("сначала делай, потом думай..")

Переводы слов с англ.:
do - делать
while - пока
*/


let i = 0;
do{
  i++;
  console.log(i);
}while(i > 5);

console.log('****************');

i = 0;
do{
  i++;
  console.log(i);
}while(i < 5);